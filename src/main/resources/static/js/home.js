function selectAltCombobox(comboboxID,hdn){
	var hidden = document.getElementById(hdn);
	comboBox = document.getElementById(comboboxID);
	if(comboBox.value == "Düzenleme Yapmak İçin Tıklayın"){
	var countVal = prompt("Kaç adet seçenek girmek istiyorsunuz? (En fazla 10 değerini girebilirsiniz)");
	var array =[];
	if(countVal>1 && countVal<11)
			{
				count = 1 + parseInt(countVal);
				for(var i=1;i<count;i++){
					var deger = prompt(i+".Seçeneği Giriniz :");
					array[i-1] = deger;
				}
			}
			else
			{
				prompt("Lütfen 1-10 arası değer girin.");
			}
	var length = comboBox.options.length;
	for (i = length-1; i >= 0; i--) {
		comboBox.options[i] = null;
	}
			for(var i=1;i<array.length+1;i++){
				var option = document.createElement("option");
				option.value = i;
				option.innerHTML = array[i-1];
				comboBox.appendChild(option);
			}
	}
			for(var i=1;i<array.length+1;i++){
				hidden.value += array[i-1].split(" ").join("") + " ";
			}
			comboBox.setAttribute("style","width:249px");
			comboBox.disabled = true;
}
